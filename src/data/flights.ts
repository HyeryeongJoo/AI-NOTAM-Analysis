/**
 * 오늘 날짜 기준 ISO-8601 시각을 생성한다.
 *
 * @param hoursOffset - 현재 시각 기준 오프셋 (시간)
 * @returns ISO-8601 문자열
 */
function todayAt(hoursOffset: number): string {
  const d = new Date();
  d.setHours(d.getHours() + hoursOffset, 0, 0, 0);
  return d.toISOString();
}

