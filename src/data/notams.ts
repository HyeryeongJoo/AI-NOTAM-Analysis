/**
 * 오늘 기준 상대적 ISO 날짜를 생성한다.
 *
 * @param daysOffset - 현재 날짜 기준 오프셋 (일)
 * @param hoursOffset - 추가 시간 오프셋
 * @returns ISO-8601 문자열
 */
function relativeDate(daysOffset: number, hoursOffset: number = 0): string {
  const d = new Date();
  d.setDate(d.getDate() + daysOffset);
  d.setHours(d.getHours() + hoursOffset, 0, 0, 0);
  return d.toISOString();
}

